<p>
  MARINS DE BORDEUS
  Marins de Bordeùs est une association créée en avril 2011 à la suite de la disparition de l' association Hissez Haut Marins de Gascogne. Elle en revendique l'esprit et l'héritage. D'abord par son nom: elle veut rappeler que Bordeaux fût (et reste!) une ville de marins et une ville gasconne. Ensuite, les Marins de Bordeùs aspirent à reprendre à leur compte l'acquis de Hissez Haut construit depuis 1990:
  - une vocation à faire connaître, sauvegarder, entretenir dans la mesure de ses
  moyens le patrimoine maritime, fluvial et portuaire local, qu'il soit matériel ou immatériel;
  -l'accueil de propriétaires de bateaux traditionnels, quel que soient leur type: servitude, pêche ou belle plaisance. Un des buts de l'association est d'aider les propriétaires dans la restauration ou l'entretien de leur bateau. Elle peut fournir des conseils techniques, éventuellement une place dans son local. Marins de Bordeùs peut légitimement revendiquer l'héritage Hissez Haut de la trentaine de bateaux restaurés ou réparés en vingt ans. On citera quelques unités notables: le Teazer, voilier anglais construit à Liverpool en 1880, le Gitana III, ancien yacht (1950) de la famille Rotschild, l'Agéna, chalutier royannais, plusieurs pilotines de lamanage, le Créa, plan Sergent des années 60, l'Aia, autre voilier anglais de 1890 ... De plus, les Marins de Bordeùs pensent reproposer la construction de petits doris(pod) en fournissant plans, éventuellement chantier et conseils aux amateurs de voile-aviron.
  -les Marins de Bordeùs ont aussi pour but de continuer de participer aux manifestations nautiques régionales ainsi qu'à leur organisation. L'association aime à rappeler que c'est Hissez Haut qui a inventé et organisé les premières Fêtes du Fleuve et de la Mer à Bordeaux ( c'était en 1995, 1996, 1997,1998 ),' accompagnées de leur célèbre course d'OFNIS (Objets Flottants Non Identifiés). L'association a repris cette jeune tradition en étant co-organisatrice des Fêtes aux Bassins en 2008 et 2009.
  -notre association veut aussi reprendre le rôle de représentant, parmi d'autres, des personnes intéressées par la vie maritime et fluviale en général et servir ainsi d'interlocuteur auprès des institutions
  -et enfin les Marins de Bordeùs veulent continuer à entretenir l'esprit d'amitié et la convivialité entre ses adhérents: repas sur le quai, apéro du 1er jeudi de chaque mois, sorties en bateau ...

