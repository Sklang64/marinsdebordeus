            <h2>Le début : <br> Hissez Haut Marins de Gascogne</h2>
            <p>L'association est née en 1989 à Meschers (port à côté de Royan).
              Créés par René Lys, Bernard Lacampagne, officier de marine marchande, Yvon Gréaud, patron lamaneur et Georges Portal.
              En 1990, l'association s'installe à Bordeaux au Hangar 26 du Bassin à flot n°2 et commence vraiment l'activité de restauration de bateaux. Une trentaine de bateaux a été restaurée en vingt ans. Le Teazer, voilier anglais de 1880, le Gitana III, ancien yacht des Rotschild, le Créa, plan Sergent des années soixante avec un beau palmarès ainsi que de nombreux bateaux de pêche.</p>
