      <h2>Héritage maritime bordelais</h2>
      <p>Les Bassins à flot ont profondément marqué l'histoire de Bordeaux et son quartier de Bacalan. Fort d'un passé maritime, industriel et marchand, le nouveau quartier répond à une nouvelle dynamique économique orientée vers le fleuve.
         Au fil du temps, le quartier a su se renouveler tout en gardant son authenticité liée à la mer.
         De la construction du premier bassin au XIXème siècle à l'élaboration du nouveau quartier résidentiel, en passant par la construction de la Base sous Marine pendant l'Occupation allemande, les Bassins à flot ont su préservé leur identité forte marquée par leur héritage maritime.</p><br>
